 This class creates and manages child dialog (dialog in another dialog).
 Can be used as alternative way to add multiple pages to dialogs.
 By default, multipage dialogs in QM are implemented by hiding groups of controls.
 This class instead creates child dialog.
 It also provides some features that also can be useful in single page dialogs.

 Features:
   Vertical scrolling when the child dialog is bigger than the provided area.
   Background color.
 

