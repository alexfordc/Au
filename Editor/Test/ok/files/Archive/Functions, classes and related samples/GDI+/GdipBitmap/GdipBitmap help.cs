 Creates, stores and auto-deletes GpBitmap object.
 Can load/save image file, display it (draw in DC), and get GDI bitmap.

 This class is inherited from GdipImage, therefore:
   The inherited functions are the same.
   Variables of GdipBitmap class can be passed to functions where need GpImage* (GDI+ flat API functions and functions of other GdipX classes).
