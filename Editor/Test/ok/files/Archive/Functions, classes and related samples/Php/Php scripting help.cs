 These functions can be used to execute PHP code in QM.

 SETUP

 1. Download PHP. I recommend to download Windows Binaries -> PHP 5.X.X installer.
 2. Run the setup program. Web server is not necessary.
 3. If PHP does not have Activescript extension (php5activescript.dll), download it separately (the link below).
 4. Move php5activescript.dll to PHP folder.
 3. Register php5activescript.dll as a COM component.
    You can register it using the Type Libraries dialog (menu Tools -> Type Libraries) or RegisterComComponent.
    If registration fails, restart QM and try to register again.

