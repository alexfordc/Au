Aš noriu ramybės.

Viskas yra gerai. Viskas bus gerai.

Aš esu ramus. Aš noriu gyventi. Gyventi yra įdomu.

Aš noriu ramiai gyventi ir dirbti. Gyvenimas yra įdomus.

Kiekvienas žmogus yra siela. Siela yra šventa. Aš gerbiu kiekvieną sielą. Kiekviena siela gerbia mane. Tik nebrandi siela gali negerbti kitų; aš tai suprantu ir atleidžiu.

Žmonės yra skirtingi. Aš pilnai priimu kitus tokius kokie jie yra. Kiti priima mane. Ignoruoju kritiką savo atžvilgiu, ir atleidžiu.

Aš linkiu visiems laimės. Tegu visos būtybės patiria laimę ir harmoniją.

Stebėk bet kokias neigiamas mintis apie save. Paneik arba nuvertink jas. Į viską žiūrėk lengvai, ir nebijok suklysti.
