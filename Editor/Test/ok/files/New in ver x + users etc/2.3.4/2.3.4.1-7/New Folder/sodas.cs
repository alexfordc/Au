TEIGIAMA
Investuoju pinigus į gerą, naudingą ir nelabai nuvertėjantį daiktą.
Įvairesnė veikla. Kai nenoriu dirbti prie kompo, ir tt, galiu dirbti sode.
Galiu laikyti katę ir šunį.
Galiu dažnai pasivaikščioti su šuniu po gamtą. Arba šiaip pasivaikščioti ir pabėgioti vakarais.
Galiu domėtis sodininkyste etc, ir pagal tai susirasti draugų.
Galiu užsiauginti daržovių. Turiu savo obuolių, vyšnių, slyvų... Galiu gamintis vegetariškus valgius iš savo išaugintų produktų.
Pasiruošimas galimai ateičiai, gyvenimui normaliame name ar sodyboje kaime arba ekobendruomenėje. Nes mieste atsibodo ir nebetenka prasmės.
Reikalui esant galiu apšiltinti ir gyventi ištisai, o butą parduoti ar išnuomoti. Mažesnės išlaidos šildymui. Užsiauginsiu dalį maisto.
Visa tai tikiuosi pagerins nuotaiką, etc.
Daryk ką nors, ir gal kažkas pasikeis į gera. Bent jau gyvenimas nebus toks nuobodus. O jei nieko nedarysi, tai greičiausisi keisis tik į bloga.


NEIGIAMA
Gal geriau būtų kitas variantas, pvz iš karto pirkti namą ar sodybą kaime ar ekogyvenvietėje. Arba susirasti draugę, ir gal ji turės.
    Bet tai būtų jau tikrai per drąsus žingsnis, reikalaujantis visų santaupų ir daugiau, o juk nežinau ar tai man patiktų, todėl kaip tik geriau iš pradžių pabandyti gyvenimą vasarą sode, apsiprasti. O dėl draugės tai pernelyg nerealu esant dabartinei būklei.
Sumažėja investicijos likvidumas.
    Bet antra vertus, apsaugo nuo infliacijos.
Gali reikti nemažai pinigų išleisti remontui etc.
    Bet jei ten gyventi nepatiks, galėsi ten negyventi ir neinvestuoti. O jei patiks, tai ne problema.
Gali būti blogi kaimynai.
    Bet tikimybė nėra didelė. Blogiausiu atveju galėsiu pamainyti į kitą sodą.
Liūdnoka perspektyva gyventi sode vienam.
    O gal kaip tik gerai tokiam vienišiui? Reikia išbandyti. Be to, galėsiu ten ir negyventi, arba tik trumpam nuvažiuoti nupjauti žolės, nuskinti vaisių, etc. Mažas skirtumas, ar pinigai stovi nenaudojami banke, ar sodas nenaudojamas. 
Važinėti dviračiu. Kartais teks prašyti Daivos pagalbos.
    Bet tai skatins išmokti vairuoti.
Padaugėtų rūpesčių kurie gali ir nepatikti.
    Bet man kaiptik ir trūksta tokių nedidelių rūpesčių.
Gali visa tai ir nepatikti.
    Bet kaip žinoti nepabandžius? Sodas daug išlaidų neprašo, ir kai reiks pinigų galėsiu parduoti.
Idėja dar gana šviežia, nesubrendusi. Reiktų dar pagalvoti apie kitus galimus variantus. Reiktų neskubėti. Skambinėti ir klausti kainų etc.
