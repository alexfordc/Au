GERIAU NEI BUTE

3 Nauda sveikatai. Mažiau nerimo ir depresijos, arba tai galima pakeisti visokiais darbeliais.
3 Tyliau viduje (nei ūžimų, nei kaimynų triukšmo už sienos). Bet lauke nėra visada tylu: netoli plentas, žolės pjovimas, šunų lojimas, gali būti kaimynas melomanus.
3 Visada galima išeiti į lauką, ten dirbti, vaikščioti po sodybą, netoli miškai. Vasarą galima net programuoti lauke ar miške. O bute tai kaip kalėjime.
2 Galima vaikščiot ir bėgiot keliukais, pievom, po mišką, pvz su šuniu.
2 Katė, šuo, vištos.
2 Pieva, medžiai, gėlės, sodas. Medžių ošimas, paukščių čiulbėjimas, gražus vaizdas.
2 Obelų, vyšnių žydėjimas pavasary, uogos vasarą.
1 Savo daržovės (bulvės, morkos, kopūstai, moliūgai, pupelės, žirniai...), vaisiai (obuoliai, vyšnios, avietės, egzotika...), kiaušiniai.
2 Vasarą vėsiau (nereikia kepti kaip bute 5 a.). Žiemą nereikia šalti, šildau kiek noriu, galiu apsišiltint namą.
1 Galima įsirengti židinį.
1 Niekas nesmirda (butas, kanalizacija, laiptinė, dujos).
2 Pinigai investuoti į daiktą kuriuo kasdien naudojuosi yra geriau nei stovintys ir nuvertėjantys. Plius pinigai už buto nuomą.
1 Tikėkimės nebevažinės tėvai su savo maistais.
2 Jei kas sugedo ar noriu patobulinti, galiu tai padaryti. O Rokišky namas problematiškas, vis reikia mokėti už remontus, sunku organizuoti remontą, triukšmas, smirda, ir nieko negaliu padaryti.
2 Naudinga patirtis. Niekada negyvenau savame name. Gal tai yra man geriausia. Nepabandęs nesužinosiu. Jei netiks, galėsiu parduoti be didelių nuostolių, nes ten yra paklausa.
2 Įdomu, nauja. Rokiškyje atsibodo.
1 Ilgu laiku turėtų būti pigesnis pragyvenimas (savas maistas, mažiau mokesčių), nors pradžiai reikės investicijų.
3 Jei nebegalėsiu ar nebenorėsiu programuoti, galimės pragyventi ir nieko nedirbant. Kaip ankstyva pensija. Nebereiks nerimauti dėl pragyvenimo. Turėsiu ką veikti ir be to.


BLOGIAU NEI BUTE

2 Jei reiks į Rokiškį, žiemą reiks važinėti autobusu į Rokiškį arba eiti 6 km.
2 Blogesnis ir brangesnis internetas. Bet gero interneto nuosavame name beveik niekur nerasi, teks susitaikyti vistiek. Sutaupysiu kitur.
1 Mažiau saugumo. Gal neplėš, nes mato kad gyvenu kukliai, nerodau turtų. Galima įrengti kameras, net nusipirkti droną, kad viską matyčiau.
1 Nejauki kaimo aura žiemą. Bet pas save galima susitvarkyti jaukiai. O gal tik reikia priprasti.
1 Gali būti mažiau patogumų. Bet galima įsirengti jei nėra gerai.


NEI GERIAU, NEI BLOGIAU

0 Namas senas, gali būti problemų ir išlaidų. Bet Rokiškyje namas taipat problematiškas, vis reikia mokėti už remontus, sunku organizuoti remontą, triukšmas, smirda, ir nieko negaliu padaryti.
0 Daugiau rūpesčių. Bet tai tik į gera.
0 Žiemą reiks kūrenti. Bet toks darbelis man yra poilsis.
0 Reiks ruošti malkas. Bet tai man yra geras sportas ir poilsis nuo pagrindinio darbo.
0 Žiemą nepastovi temperatūra. Bet galima pasidaryti kiek noriu, nereikia šalti kaip bute prie 18 ar 16 laipsnių. Be to, galima įsirengti katilą kur palaiko pastovią temperatūtą.
0 Reiks pasirūpinti dujų balionu. Bet netoli atsivežti iš Laibgalių. Galima užsakyti kad atvežtų. Arba paprašyti giminių kad atveštų. Gal bus pigiau nei Rokiškyje, nes nėra abonentinio mokesčio. Arba nusipirkti elektrinę viryklę.
