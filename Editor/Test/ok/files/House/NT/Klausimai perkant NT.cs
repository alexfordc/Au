PATARIMAI ir PRIMINIMAI

Bendravimas su pardavėju (savininku ar NT agentu):  Jam nepalankią (ar „nesvarbią“) informaciją reikės traukti ilgai ir nuobodžiai. Nesvarbu, kas parašyta skelbime. Reikia persiklausti dėl kiekvieno ten įrašyto punkto.

Namą pirmiausiai apžiūrėti be agento, nes, kai viskas įvyksta staigiai, sunkiau situaciją įvertinti racionaliai. Pravartu apžiūrėti skirtingomis oro sąlygomis (sausu metu, po/per liūtis).

Atidžiai perskaityti notaro sudarytą sutartį. Peržiūrėti dokumentus.

Pirkėjas turi būti tikras būsto likvidumu ir įvertinti riziką, jeigu ateityje prarastų galimybę išlaikyti būstą ar  norėtų jį pakeisti kitu. Toliau nuo centro esančių būstų likvidumas yra mažesnis, bet ir vertė mažesnė.

Pirma pasirašoma preliminari pirkimo – pardavimo sutartis. Jos pagrindu vėliau pasirašoma pagrindinė (notarinė) pirkimo – pardavimo sutartis. Preliminaria sutartimi įforminami derybomis pasiekti esminiai susitarimai (galutinė kaina, atsiskaitymo sąlygos ir terminai, avansiniai mokėjimai, užstatas, persikraustymo terminai, notaro ir kiti galimi mokesčiai, paliekami baldai ir įranga, kitos sąlygos). Sandoris laikomas įvykusiu, kai notaras patvirtina pirkėjo įsipareigojimą pardavėjui pagal pirkimo – pardavimo sutartį ir išrašo liudijimą.

Jei ieškote būsto kitame mieste ar jums nepažįstamame rajone, būtinai pasiklausinėkite senyvo amžiaus žmonių - jie jums suteiks naudingos informacijos ir, be to, yra lengvai sukalbami ir mėgsta pasišnekėti.

Rekomenduojama būstą rinktis konsultuojantis su nepriklausomais specialistais, kurie galėtų įvertinti ir pasakyti, kiek reikės papildomų investicijų būsto kokybei išlaikyti ar pagerinti.

Pradžioje skambink ir kalbėkis su mažiau patinkančių namų pardavėjais, tiek savininkais tiek NT agentais, kad įgautum bendravimo patirties ir pasitikėjimo savimi.

