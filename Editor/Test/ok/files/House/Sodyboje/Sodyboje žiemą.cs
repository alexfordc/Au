Rytas, pvz 7:00.
Kadangi galėjau eiti miegoti laiku (netrukdė kaimynai), tai ir keltis galima laiku.
Kodėl turėčiau norėti keltis?
	Katės turbūt nebemiega, reikia pamaitinti, išleisti į lauką.
	Reikia užkurti krosnį ar katilą.
	Galimės ramiai programuoti, ir/ar dirbti kitus darbelius:
		Nukasti sniegą.
		Eiti pasivaikščioti su šuniu.
		Kurstyti krosnį ar katilą.
		Eiti į mišką malkauti.
		Skaldyti malkas.
		Kalti inkilus.
		Daryti lesyklą, ten dėti maisto paukščiams, stebėti.
		Prižiūrėti kates, šunį, vištas, gal dar ką nors. Glostyti, žaisti, džiaugtis.
		Tvarkyti ir valyti kambarius. Skalbti skalbinius.
		Daryti smulkius remontus name, pvz tapetuoti.
		Prižiūrėti kambarinius augalus. Ne tik laistyti, bet ir domėtis auginimu, domėtis naujais augalais.
		Rinkti informaciją apie augalus kuriuos pavasarį norėsiu pasodinti sodyboje.
		Apsaugoti vaismedžius nuo šalčio ir kiškių.
		Išrinkti supuvusius obuolius.
		Rauginti kopūstus.
		Kartais pasidaryti naujų valgių.
	O kodėl turėčiau norėti visa tai daryti?
		Todėl kad dirbant tokius darbelius jaučiuosi geriau nei nieko neveikiant.
		O jei kada nenorėsiu, galimės padaryti tik būtinus darbelius, o tada ramiai snausti ar žiūrėti filmus.
		Katės ir kiti naminiai (ir laukiniai) gyvūnai, kuriais rūpinuosi, suteiks daugiau prasmės gyvenimui. Taipat suteiks šilumos.
		O ko daugiau dar galėčiau norėti iš gyvenimo? Su mano patirtimi ir galimybėmis, toks gyvenimas man yra ko gero idealus. Svarbu tik kad būtų gera sveikata iki mirties.
	Taipat galėsiu daryti ir tai ką mėgstu dabar, pvz žiūrėti dokumentinius filmus. Bus smagiau nei dabar, nes dabar to yra per daug, o tada bus subalansuota.
	Manau kad atsiras ir daugiau užsiėmimų ir rūpesčių, taip kad nebus laiko tinginiauti ir depresuoti kaip dabar.

Ar tikrai man tai patiks? Ar nenusibos?
O ar turiu geresnių alternatyvų?
	Atrodo kad ne.
	Nebent atsiras ten begyvenant. Nepanašu kad atsirastų gyvenant bute.
Bet gi nereiks visą laiką tik tai ir daryti. Subalansuosiu tai su darbu prie kompiuterio, ir bus labai gerai.
