ar žinojote, kad šiandien 98 proc. visų mokėjimų yra elektroniniai, o išmaniuoju telefonu pinigų pervedama dažniau nei banko padalinyje?
Todėl netrukus keisime kai kuriuos įkainius bei skelbsime laisvę elektroniniams mokėjimams! Nuo 2016-12-01 pristatysime „Swedbank“ paslaugų planus, kuriuose – neriboti populiariausi elektroniniai mokėjimai. Paslaugų plano kaina – nuo 1,70 Eur/mėn.

Užsisakę Paslaugų planą, mokėsite vieną mėnesio mokestį už:

    visus elektroninius pervedimus eurais į bankus Lietuvoje bei Europos ekonominės erdvės (EEE) šalyse (įskaitant įmokas ir e. sąskaitų apmokėjimą);
    visų Jūsų sąskaitų atidarymą, tvarkymą, uždarymą;
    visų pervedimų eurais, gautų iš Lietuvos ir EEE bankų, įskaitymą į Jūsų sąskaitas (toliau – pervedimų įskaitymas);
    bekontaktę kortelę „Debit MasterCard“ arba Jūsų turimą debeto kortelę.

Paslaugų planą nuo gruodžio 1 d. galėsite užsisakyti interneto banke, tel. 1884 arba banko padalinyje. Gruodžio mėnesį paslaugų planais galėsite naudotis nemokėdami mėnesio mokesčio, jis bus pradėtas taikyti nuo 2017-01-01. Plačiau apie planus.

Nuo 2017-01-01 keisis ir kiti įkainiai. Nebebus taikomi atskiri mokesčiai už pervedimų įskaitymą bei už sąskaitų, nesusietų su mokėjimo kortele, tvarkymą. Sąskaitų atidarymas, tvarkymas ir uždarymas bei pervedimų įskaitymas bus įskaičiuoti į minimalų kasdienių paslaugų mokestį, kuris sieks 0,70 Eur/mėn. ir bus taikomas kiekvienai sąskaitai. Užsisakius bet kurį Paslaugų planą, minimalaus kasdienių paslaugų mokesčio mokėti nereikės.

Taip pat keisis kiti įkainiai: įmokų mokėjimo interneto banke, operacijų grynaisiais pinigais banko padaliniuose ir kt. Su visais pokyčiais ir kita klientams aktualia informacija galite susipažinti čia.

Kilus klausimų, susisiekite su mumis interneto banko žinute arba tel. 1884.




