Expanded Case Statement
=======================
Conditional case:
eg. in "sel x", test if x is one of three numbers:

sel x; case 1 or 2 or 3: out "x is 1 or 2 or 3"
	
(Im currently learning Linux, where the Bash shell syntax
of case is sorta neat, with a "|" separater to represent "or", and
statement starts with "case <word> in" instead of "sel", where
<word> can represent a variable or command output or...)
