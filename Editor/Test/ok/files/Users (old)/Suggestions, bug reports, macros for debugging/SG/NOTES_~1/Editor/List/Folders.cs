 Enabled macro in disabled folder stopped working when upgraiding
 ================================================================

After instaling QM2.1.0, one trigger i used daily stopped working.
Discovered that the macro was in a folder whose all items had been disabled except
that one macro i used.

I believe i had disabled the items in 2.0.9 using Right-click on folder then Disable,
(and then re-enabled the one macro)

Therefore In QM 2.1.0 i believe that Qm marked the folder as disabled.


Seems ok in theory, but can be frustrating for people who change from Qm2.0.9 to qm2.1.0 and 
discover that some macros wont work.

Suggestion: Dont mark folders as "disabled" when user updrades from 2.0.9

