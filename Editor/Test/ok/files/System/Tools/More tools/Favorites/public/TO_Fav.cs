function $name [action1] [action2]

 Opens a favorite dialog and optionally selects an action.

 name - name of function that implements the dialog.
 action1 - 0-based index of action list (listbox or combobox, if any) item.
 action2 - can be used with dialogs that support two actions.


mac name "" 0 0 action1 action2
