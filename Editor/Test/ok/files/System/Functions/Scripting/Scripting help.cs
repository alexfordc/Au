 The Vbs... and Js... functions allow you to use VBScript and JScript in QM.
 Similar functions for <link "http://www.quickmacros.com/forum/viewtopic.php?f=2&t=2387">PHP</link> and <link "http://www.quickmacros.com/forum/viewtopic.php?f=2&t=1641">Perl</link> are available in the forum.

 You can <google "Microsoft Windows Script Technologies, WSH">download</google> Microsoft Scripting Technologies help file.
 It includes user's guide and reference for VBScript, JScript,
 Windows Scripting Runtime and Windows Script Host.
 There you also will find latest versions of Windows scripting components.

 The Ps... functions make easier to run Windows PowerShell scripts.

 The Cs... functions and CsScript class allow you to use C# and VB.NET in QM.

 The __Tcc class allows you to use C in QM. The C compiler also supports inline assembler.
